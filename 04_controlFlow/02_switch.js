const month="feb";
switch(month)
{
    case "jan":
        console.log("January");
        break;
    case "feb":
        console.log("february");
        break;
    case "march":
        console.log("march");
        break;
    case "april":
        console.log("april");
        break;
    case "june":
        console.log("june");
        break;
    case "july":
        console.log("july");
        break;
    default:
        console.log("Invalid month entered.");
}